    public void setDefaultTable(){
         tblModel = new DefaultTableModel();
         tabel_matpel.setModel(tblModel);
         tblModel.addColumn("Kode Matpel");
         tblModel.addColumn("Nama Matpel");
         tblModel.addColumn("KKM");
         try{
             java.sql.Connection conn = (java.sql.Connection)rapsis_online.koneksi.konekDb();
             java.sql.Statement stm = conn.createStatement();
             java.sql.ResultSet rs = stm.executeQuery("select * from tbl_mat_pel");
             while(rs.next()){
                data [0] = rs.getString(1);
                 data [1] = rs.getString(2);
                 data [2] = rs.getString(3);
                 tblModel.addRow(data);
             }
             stm.close();
             rs.close();
         }catch(Exception e){
             System.out.println("Data gagal disimpan"+e.getMessage());
         }
        
     }

