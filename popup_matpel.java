    private void txt_matpelKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_matpelKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()== evt.VK_ENTER){
            hapusTabel();
            String Key;
            if(cmb_matpel.getSelectedItem().toString().equals("Kode Matpel")){
                Key ="kd_matpel";
            }else{
                Key ="nm_matpel";
            }
            try{
                java.sql.Connection conn = (java.sql.Connection)rapsis_online.koneksi.konekDb();
                java.sql.Statement stm = conn.createStatement();
                java.sql.ResultSet rs = stm.executeQuery("select kd_matpel,nm_matpel,kkm from tbl_mat_pel  where "+Key+" like '%"+txt_matpel.getText()+"%'");
                while(rs.next()){
                    data [0] = rs.getString(1);
                    data [1] = rs.getString(2);
                     data [2] = rs.getString(3);
                    tblModel.addRow(data);
                    
                }
                rs.close();
                stm.close();
                
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Error"+e);
            }
        }
    }//GEN-LAST:event_txt_matpelKeyPressed

    private void tabel_matpelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_matpelMouseClicked
        // TODO add your handling code here:
         int row = tabel_matpel.getSelectedRow();
        if(evt.getClickCount()== 1){
            matpel.setDataPopup(new String []{
                tblModel.getValueAt(row, 0).toString(),
                tblModel.getValueAt(row, 1).toString(),
                tblModel.getValueAt(row, 2).toString()
            });
            }
        this.dispose();
    }//GEN-LAST:event_tabel_matpelMouseClicked
public String [] getData(){
    return matpel.getDataPopup();
    
}
public void hapusTabel(){
    int rowCount = tabel_matpel.getRowCount();
    for(int i = 0; i < rowCount;i++){
        tblModel.removeRow(0);
    }
}
